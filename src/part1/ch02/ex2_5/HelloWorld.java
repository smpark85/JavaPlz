package part1.ch02.ex2_5;

public class HelloWorld {
    public static void main(String[] args) {
        String message = "헬로 월드!"; //변수 생성 및 초기화
        System.out.println(message); // 출력할 변수명

        message = "웰컴 투 헬!"; //새로 대입할 값
        System.out.println(message); //출력할 변수명 (바뀐값이 출력됨)
    }
}
